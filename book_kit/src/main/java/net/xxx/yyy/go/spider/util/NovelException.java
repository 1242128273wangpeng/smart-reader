package net.xxx.yyy.go.spider.util;

public class NovelException extends RuntimeException {
    private static final long serialVersionUID = -5758127013647427605L;

    public NovelException() {
        super();
    }

    public NovelException(String message) {
        super(message);
    }

    public NovelException(Throwable cause) {
        super(cause);
    }

    public NovelException(String message, Throwable cause) {
        super(message, cause);
    }
}
